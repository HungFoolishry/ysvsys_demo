    class LRUCache {
        LruList list;
        Map<Integer, Node> map;
        int capacity;

        public LRUCache(int capacity) {
            list = new LruList();
            map = new HashMap<>();
            this.capacity = capacity;
        }

        public int get(int key) {
            if (!map.containsKey(key)) {
                return -1;
            }
            Node node = map.get(key);
            list.del(node);
            list.addRecent(node);
            return node.val;
        }

        public void put(int key, int value) {
            Node node = new Node(key, value);
            if (map.containsKey(key)) {
                list.del(map.get(key));
            }
            map.put(key, node);
            list.addRecent(node);
            if (list.getSize() > capacity) {
                Node last = list.removeLast();
                map.remove(last.key);
            }

        }

        class Node {
            Integer key;
            Integer val;
            Node next;
            Node pre;

            public Node(Integer key, Integer val) {
                this.key = key;
                this.val = val;
            }
        }

        class LruList {
            Node head;
            Node tail;
            int size;

            LruList() {
                head = new Node(null, null);
                tail = new Node(null, null);;
                head.next = tail;
                tail.pre = head;
            }

            public int getSize() {
                return size;
            }

            public void del(Node node) {
                //æ˜“é”™ç‚¹ å‰åè¦çœ‹æ¸…
                node.pre.next = node.next;
                node.next.pre = node.pre;
                node.next = null;
                size--;

            }

            public void addRecent(Node node) {
                Node tmp = head.next;
                head.next = node;
                node.next = tmp;
                node.pre = head;
                tmp.pre = node;
                size++;

            }

            public Node removeLast() {
                Node tmp = tail.pre;
                del(tmp);
                return tmp;
            }
        }
    }

